    private void changeTable() {
        if (jComboBox1.getSelectedItem().equals("Book")) {
            Manageable book = new BookData(jTable1, connect);
            book.showData();
        } else if (jComboBox1.getSelectedItem().equals("Member")) {
            Manageable member = new MemberData(jTable1, connect);
            member.showData();
        } else if (jComboBox1.getSelectedItem().equals("Borrow")) {
            Manageable borrow = new BorrowData(jTable1, connect);
            borrow.showData();
        }
    }
